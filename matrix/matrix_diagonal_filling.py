n, m = map(int, input().split())
number = 1
matrix = [[0 for _ in range(m)] for _ in range(n)]
for s in range(m + n):
    for i in range(n):
        for j in range(m):
            if i + j == s:
                matrix[i][j] = str(number).ljust(3)
                number += 1

for row in matrix:
    print(*row)
